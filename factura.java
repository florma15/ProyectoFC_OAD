    public void combo() throws SQLException {
        bd1 = new accesoBD("localhost", usr, clave, bd);
        bd1.conectabdcl();
        ArrayList<String> a = new ArrayList<>();
        ResultSet rs;
        rs = bd1.mostrar("select producto.cod_pro from producto join factura on factura.id_pro");
        while (rs.next()) {
            a.add(rs.getString("cod_pro"));
        }
        productolist.removeAllItems();
        for (int i = 0; i < a.size(); i++) {
            productolist.addItem(a.get(i));
        }
    }

