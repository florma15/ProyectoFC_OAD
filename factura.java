    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (codpro.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese un producto");
        } else {
            double a, b;
            String bus = codpro.getText();
            prepre = prepre + (Float.parseFloat(cant.getValue().toString())* Float.parseFloat(pre.getText()));
            subtotal.setText(String.valueOf(prepre));
            a = prepre * 0.12;
            iva.setText(String.valueOf(a));
            b = prepre + a;
            total.setText(String.valueOf(b));
           
            cant.setValue(0);
            try {
                bd1.restarpro(bus);
            } catch (SQLException ex) {
                Logger.getLogger(factura.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        bd1 = new accesoBD("localhost", usr, clave, bd);
        bd1.conectabdcl();
        int codcli = 0, codpro = 0;
        ResultSet cl, pr;
        if (ced.getText().length() >= 10 && pro.getText().length() != 0 && total.getText().length() != 0) {
            try {
                cl = bd1.mostrarbdbc(ced.getText());
                pr = bd1.mostrarbdpro(pro.getText());
                while (cl.next()) {
                    codcli = cl.getInt("cod_cli");
                }
                while (pr.next()) {
                    codpro = pr.getInt("id_pro");
                }

                bd1.factura(codcli, codpro, Double.parseDouble(total.getText()));
                ced.setText(null);
                nomb.setText(null);
                pro.setText(null);
                subtotal.setText(null);
                iva.setText(null);
                total.setText(null);
                this.codpro.setText(null);
                desc.setText(null);
                pre.setText(null);
                cant.setValue(0);
            } catch (SQLException ex) {

            }

        } else {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }
    }//GEN-LAST:event_guardarActionPerformed

