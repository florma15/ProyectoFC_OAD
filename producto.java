    private void coddisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_coddisActionPerformed
        String nom;
        bd1 = new accesoBD("localhost", usr, clave, bd);
        bd1.conectabdcl();
        nom = coddis.getSelectedItem().toString();
        ResultSet rs;
        try {
            rs=bd1.mostrar("select distribuidor.cod_dis from distribuidor join producto on distribuidor.cod_dis where nombre ='"+nom+"'");
            while(rs.next()){
                districo.setText(String.valueOf(rs.getInt(1)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(producto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_coddisActionPerformed

