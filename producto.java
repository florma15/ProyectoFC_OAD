    public void combo() throws SQLException {
        bd1 = new accesoBD("localhost", usr, clave, bd);
        bd1.conectabdcl();
        ArrayList<String> a = new ArrayList<>();
        ResultSet rs;
        rs = bd1.codis();
        while (rs.next()) {
            a.add(rs.getString("nombre"));
        }
        coddis.removeAllItems();
        for (int i = 0; i < a.size(); i++) {
            coddis.addItem(a.get(i));
        }
    }

